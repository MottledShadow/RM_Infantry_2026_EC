/**
 ******************************************************************************
 * @file    chassis.h
 * @brief   底盘控制头文件（控制层）
 * @note    层级说明：
 *            本文件属于【控制层】，位于设备驱动层之上、应用层之下。
 *            依赖链（单向向下）：
 *              chassis → pid（算法层）
 *              chassis → lpf（算法层）
 *              chassis → motor_driver（设备驱动层，读 g_motor_3508）
 *              chassis → rsi_driver（设备驱动层，读热量/功率数据）
 *              chassis → bsp_can（BSP 层，发 3508 转矩）
 *
 *          底盘结构：
 *            四轮全向麦克纳姆轮，45° 安装角，3508 电机驱动。
 *            运动学坐标系：右手系，X 前，Y 左，W 逆时针为正。
 *
 *          运动学逆解（麦轮 45° 安装）：
 *            θ = YAW + π/4（云台到底盘坐标系旋转角）
 *            V1 =  cos(θ)·vx - sin(θ)·vy + vw   （左前）
 *            V2 =  sin(θ)·vx + cos(θ)·vy + vw   （右前）
 *            V3 = -cos(θ)·vx + sin(θ)·vy + vw   （右后）
 *            V4 = -sin(θ)·vx - cos(θ)·vy + vw   （左后）
 *
 *          功率控制策略：
 *            估算当前底盘功率 = Σ|滤波后电流(A)| × 标称电压
 *            若功率超出限制裕量区间，用缓冲能量动态缩放输出系数 k
 *            k ∈ (0, 1]，乘以速度环 PID 输出，实现软性功率限制。
 *
 * @version 1.0
 * @date    2026-4-6
 * @author  MOS
 ******************************************************************************
 */

#ifndef CHASSIS_H
#define CHASSIS_H

#include "pid.h"
#include "lpf.h"
#include <math.h>
#include "motor_driver.h"   /* 设备驱动层：g_motor_3508 读取            */
#include "rsi_driver.h"     /* 设备驱动层：g_rsi_power_heat / g_rsi_robot_status */
#include "bsp_can.h"        /* BSP 层：CAN 发送（控制层 → BSP，方向正确）*/
#include <math.h>           /* cosf, sinf, fabsf                         */

/* ========================================================================== */
/*                          底盘机械参数                                        */
/* ========================================================================== */

/** M3508 电机减速比（3591/187 ≈ 19.2）*/
#define CHASSIS_REDUCTION_RATIO     (3591.0f / 187.0f)

/** 麦克纳姆轮安装角（相对底盘中轴线，45°）*/
#define CHASSIS_WHEEL_ANGLE_RAD     (CHASSIS_PI / 4.0f)

/** 底盘电机数量 */
#define CHASSIS_MOTOR_COUNT         4U

/* ========================================================================== */
/*                              平移速度上限                                    */
/* ========================================================================== */

/** 底盘最大平移速度（rpm）*/
#define CHASSIS_MAX_TRANSLATIONAL_SPEED  500.0f

/* ========================================================================== */
/*                              功率控制参数                                    */
/* ========================================================================== */

/**
 * @brief  裁判系统未就绪时的功率上限 Fallback 值（J）
 * @note   仅在调试模式（裁判系统未连接）下生效。
 *         正式比赛时此值永远不会被使用（裁判系统就绪后每帧覆盖）。
 */
#define CHASSIS_FALLBACK_LIMIT_POWER    120.0f

/**
 * @brief  M3508 电流原始值转安培系数
 * @note   M3508 actual_quadrature_current 范围 [-16384, 16384] 对应 [-20A, 20A]
 *         系数 = 16384 / 20 = 819.2
 */
#define CHASSIS_CURRENT_TO_AMP      819.2f

/** 底盘标称供电电压（V）*/
#define CHASSIS_NOMINAL_VOLTAGE_V   24.0f

/**
 * @brief  功率限制裕量（W）
 * @note   当估算功率超过 (chassis_power_limit - POWER_MARGIN) 时开始限制，
 *         而非等到严格超限才动作，提供提前量防止超标。
 */
#define CHASSIS_POWER_MARGIN_W      20.0f

/**
 * @brief  缓冲能量利用系数
 * @note   k = (1/power) × buffer_energy × COEFF
 *         COEFF 越大，允许在缓冲能量充足时输出更高功率。
 *         推荐范围 [1.0, 2.0]，超过 2.0 可能导致缓冲能量快速耗尽。
 */
#define CHASSIS_BUFFER_ENERGY_COEFF 1.5f

/* ========================================================================== */
/*                              PID 参数（3508 速度环）                         */
/* ========================================================================== */

/** 四个 3508 使用相同参数（底盘各轮物理特性相同）*/
#define CHASSIS_SPEED_KP            320.0f
#define CHASSIS_SPEED_KI            20.0f
#define CHASSIS_SPEED_KD            0.0f
#define CHASSIS_SPEED_IOUT_MAX      1000.0f /**< 速度环积分限幅（电流值）*/
#define CHASSIS_SPEED_OUT_MAX       16000.0f/**< 速度环输出限幅（电流值）*/
#define CHASSIS_SPEED_IGAP          5.0f    /**< 速度环积分分离阈值      */

/* ========================================================================== */
/*                          LPF 参数（转矩电流滤波）                            */
/* ========================================================================== */

/**
 * @brief  功率估算用低通滤波器参数
 * @note   对 actual_quadrature_current 滤波后再估算功率，
 *         消除电流测量噪声对功率限制系数 k 的抖动影响。
 */
#define CHASSIS_LPF_DT              0.001f  /**< 滤波器采样周期（s），须与控制周期一致 */
#define CHASSIS_LPF_FC              100.0f  /**< 截止频率（Hz）                         */

/* ========================================================================== */
/*                              数学常量                                        */
/* ========================================================================== */

#ifndef CHASSIS_PI
#define CHASSIS_PI  3.14159265358979323846f
#endif

/* ========================================================================== */
/*                              函数声明                                        */
/* ========================================================================== */

/**
 * @brief  底盘控制模块初始化
 * @note   初始化四路速度 PID 和转矩电流低通滤波器。
 *         须在进入主控制循环前调用。
 * @retval 无
 */
void Chassis_Init(void);

/**
 * @brief  底盘控制主函数（每帧调用，须以固定频率调用）
 * @note   执行流程：
 *           1. 麦轮运动学逆解（底盘系 → 各轮目标转速）
 *           2. 功率限制系数计算
 *           3. 速度环 PID 计算（含功率缩放）
 *           4. 打包 CAN 帧发送电流指令
 * @param  v_x          前向速度指令（底盘坐标系，X 轴正方向为前）
 * @param  v_y          侧向速度指令（底盘坐标系，Y 轴正方向为左）
 * @param  v_w          旋转速度指令（逆时针为正）
 * @param  yaw_gimbal_to_chassis  云台相对底盘的偏转角（rad），
 *                                用于将速度指令从云台坐标系转换到底盘坐标系。
 *                                底盘正对云台时为 0，逆时针为正。
 * @retval 无
 */
void Chassis_Control(float v_x, float v_y, float v_w, float yaw_gimbal_to_chassis);

#endif /* CHASSIS_H */
