/**
 * @brief  读取弹丸检测光电门状态，并完成极性转换
 */
uint8_t BSP_GPIO_ReadShotDetect(void)
{
    /* 光电门低电平有效：GPIO_PIN_RESET = 遮光 = 弹丸通过
     * 取反后：返回 1 = 有弹丸，与 Key_Scan 的 KEY_LEVEL_PRESSED 约定一致 */
    return (HAL_GPIO_ReadPin(SHOT_DETECT_GPIO_PORT, SHOT_DETECT_GPIO_PIN)
            == GPIO_PIN_RESET) ? 1U : 0U;
}
