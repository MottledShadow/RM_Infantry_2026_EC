/**
 ******************************************************************************
 * @file    lpf.h
 * @brief   一阶低通滤波器头文件（算法层）
 * @note    层级说明：
 *            本文件属于【算法层】，无任何硬件依赖，可在任意平台复用。
 *            仅依赖标准库 <math.h>，禁止 include 任何 BSP / HAL 头文件。
 *
 *          算法说明（一阶 RC 低通滤波器离散化）：
 *            连续域传递函数：H(s) = 1 / (RC·s + 1)，RC = 1/(2π·fc)
 *            前向欧拉离散化：
 *              α   = dt / (RC + dt)
 *              y[n] = α·x[n] + (1-α)·y[n-1]
 *            α 越小，截止频率越低，滤波越强，但响应越慢。
 *
 * @version 1.0
 * @date    2026-4-5
 * @author  MOS
 ******************************************************************************
 */

#ifndef LPF_H
#define LPF_H

#include <math.h>

/* ========================================================================== */
/*                              数学常量                                        */
/* ========================================================================== */

#ifndef LPF_PI
#define LPF_PI  3.14159265358979323846f  /**< 圆周率（算法层自定义，避免依赖 bsp.h）*/
#endif

/* ========================================================================== */
/*                              数据结构定义                                    */
/* ========================================================================== */

/**
 * @brief  一阶低通滤波器状态结构体
 * @note   使用前必须调用 LPF_Init() 初始化，切勿直接赋值 a_coef / b_coef。
 */
typedef struct
{
    float input;        /**< 当前输入值                     */
    float output;       /**< 当前滤波输出值                 */
    float last_output;  /**< 上一拍输出值（滤波器状态变量）*/
    float a_coef;       /**< 系数 α = dt/(RC+dt)           */
    float b_coef;       /**< 系数 (1-α)，与 a_coef 之和为 1 */
    float dt;           /**< 采样周期，单位 s               */
    float fc;           /**< 截止频率，单位 Hz              */
} LowPassFilter_t;

/* ========================================================================== */
/*                              函数声明                                        */
/* ========================================================================== */

/**
 * @brief  初始化低通滤波器，计算并缓存系数
 * @param  lpf  滤波器实例指针
 * @param  dt   采样周期（s），须与实际调用 LPF_Update() 的周期一致
 * @param  fc   截止频率（Hz），须满足 fc < 1/(2·dt)（奈奎斯特）
 * @retval 无
 */
void LPF_Init(LowPassFilter_t *lpf, float dt, float fc);

/**
 * @brief  输入新采样值，更新滤波器状态并返回滤波结果
 * @param  lpf    滤波器实例指针
 * @param  input  本拍原始输入值
 * @retval 当前拍滤波输出值
 */
float LPF_Update(LowPassFilter_t *lpf, float input);

#endif /* LPF_H */
