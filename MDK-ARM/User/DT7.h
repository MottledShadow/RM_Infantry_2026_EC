/**
 ******************************************************************************
 * @file    DT7.h
 * @brief   DT7 遥控器应用控制头文件（应用层）
 * @note    层级说明：
 *            本文件属于【应用层】，是整个工程最顶层的调度模块之一。
 *            职责：将 DT7 遥控器的摇杆/拨杆输入翻译为控制层指令。
 *            依赖链（单向向下）：
 *              DT7 → chassis（控制层）
 *              DT7 → gimbal（控制层）
 *              DT7 → shooter（控制层）
 *              DT7 → bsp_can（BSP 层，紧急禁用时直接发零帧）
 *
 *          拨杆状态定义（DJI DT7 遥控器）：
 *            s[0]（左拨杆）：1=上, 3=中, 2=下
 *            s[1]（右拨杆）：1=上, 3=中, 2=下
 *
 *          控制模式映射：
 *            s[0]=1（上）→ 全功能模式（云台+底盘+摩擦轮）
 *                s[1]=1（上）→ 调试：直接发送固定转矩给 M2006
 *            s[0]=3（中）→ 云台+底盘（摩擦轮不启动）
 *            s[0]=2（下）→ 安全模式（所有电机清零）
 *
 * @version 1.0
 * @date    2026-4-6
 * @author  MOS
 ******************************************************************************
 */

#ifndef DT7_H
#define DT7_H

#include "chassis.h"
#include "gimbal.h"
#include "shooter.h"
#include "dt7_driver.h"  /* DT7_ctrl_t 定义 */
#include "bsp_can.h"  /* BSP 层：安全模式下直接发零帧（应用层→BSP，紧急路径）*/

/* ========================================================================== */
/*                          DT7 通道索引（ch[] 数组下标）                        */
/* ========================================================================== */

#define DT7_CH_YAW      0U  /**< 右摇杆水平轴 → Yaw  速度指令 */
#define DT7_CH_PITCH    1U  /**< 右摇杆垂直轴 → Pitch 速度指令 */
#define DT7_CH_CHASSIS_Y 2U /**< 左摇杆垂直轴 → 底盘纵向速度   */
#define DT7_CH_CHASSIS_X 3U /**< 左摇杆水平轴 → 底盘横向速度   */


/** 摇杆中值（对应物理中位）*/
#define DT7_CH_CENTER           1024

/** 将通道原始值转换为以中值为零点的偏差量 */
#define DT7_CH_OFFSET(ctrl, idx)  ((ctrl)->rc.ch[(idx)] - DT7_CH_CENTER)

/* ========================================================================== */
/*                          拨杆状态定义                                        */
/* ========================================================================== */

#define DT7_SWITCH_UP   1U  /**< 拨杆拨到上方 */
#define DT7_SWITCH_MID  3U  /**< 拨杆居中     */
#define DT7_SWITCH_DOWN 2U  /**< 拨杆拨到下方 */

/* ========================================================================== */
/*                          底盘随动参数                                        */
/* ========================================================================== */

/**
 * @brief  Yaw 角死区（rad）
 * @note   云台偏转角绝对值小于此值时认为已对齐，旋转速度归零，
 *         避免微小误差引起持续抖动。
 */
#define DT7_YAW_DEADBAND                0.0003f

/**
 * @brief  底盘随动增益（rad → 旋转速度）
 * @note   rotational_velocity = -yaw_angle × VTM_YAW_FOLLOW_GAIN
 *         增益越大，随动响应越快，但可能引起振荡。TODO: 根据实际调试调整。
 */
#define DT7_YAW_FOLLOW_GAIN             200.0f

/* ========================================================================== */
/*                          云台灵敏度（摇杆→角速度系数）                        */
/* ========================================================================== */

/** 摇杆偏差值 × 此系数 = 云台角速度指令（rad/s）TODO: 根据实际手感调整 */
#define DT7_GIMBAL_SENSITIVITY      0.01f

/* ========================================================================== */
/*                          调试模式参数                                        */
/* ========================================================================== */

/**
 * @brief  调试模式下直接发送给 M2006 的固定转矩电流值
 * @note   此模式绕过 shooter.c 的热量控制，仅用于硬件调试。
 */
#define DT7_DEBUG_M2006_TORQUE      1000U

/* ========================================================================== */
/*                              函数声明                                        */
/* ========================================================================== */

/**
 * @brief  DT7 遥控器应用控制函数（每帧调用）
 * @note   根据左拨杆（s[0]）状态分发控制指令：
 *           上：全功能运行；中：运动控制；下：安全停机。
 *         须以固定频率（1kHz）在定时器中断中调用。
 * @param  dt7_ctrl  DT7 遥控器数据结构指针（由 dt7_driver 解析后传入）
 * @retval 无
 */
void DT7_Control(DT7_ctrl_t *dt7_ctrl);

#endif /* DT7_H */
