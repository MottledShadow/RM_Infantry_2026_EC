/**
 ******************************************************************************
 * @file    key_scan.h
 * @brief   软件按键消抖与状态机头文件（算法层）
 * @note    层级说明：
 *            本文件属于【算法层】。
 *            输入为已经数字化的电平值（0/1），与具体 GPIO 完全解耦，
 *            可在任意平台复用，无任何硬件依赖。
 *
 *          状态机逻辑（Toggle 型按键，按下一次翻转一次输出状态）：
 *
 *            STATE_IDLE(0) ──按下──> STATE_PRESS_DEBOUNCE(1)
 *                                         │ 计数≥DEBOUNCE_CNT 且仍按下
 *                                         ▼
 *                                    STATE_HELD(2)
 *                                         │ 松开
 *                                         ▼
 *                                    STATE_RELEASE_DEBOUNCE(3)
 *                                         │ 计数≥DEBOUNCE_CNT 且仍松开
 *                                         ▼
 *                                   翻转 *out_state，回到 STATE_IDLE
 *
 *          调用方式：在周期性任务中（建议 1ms 定时器）调用 Key_Scan()，
 *                    传入当前 GPIO 电平和输出状态指针即可。
 *
 * @version 1.0
 * @date    2026-4-5
 * @author  MOS
 ******************************************************************************
 */

#ifndef KEY_SCAN_H
#define KEY_SCAN_H

#include <stdint.h>

/* ========================================================================== */
/*                              消抖参数                                        */
/* ========================================================================== */

/**
 * @brief  消抖计数阈值（单位：调用周期数）
 * @note   若以 1ms 周期调用，则 KEY_DEBOUNCE_CNT = 20 对应 20ms 消抖时间。
 *         建议范围：10~50。
 */
#define KEY_DEBOUNCE_CNT    20U

/* ── 电平极性 ── */
#define KEY_LEVEL_PRESSED   1U  /**< 按下时 GPIO 电平（高有效）*/
#define KEY_LEVEL_RELEASED  0U  /**< 松开时 GPIO 电平          */

/* ========================================================================== */
/*                              状态枚举                                        */
/* ========================================================================== */

/**
 * @brief  按键状态机内部状态
 */
typedef enum
{
    KEY_STATE_IDLE             = 0U, /**< 空闲：等待按下         */
    KEY_STATE_PRESS_DEBOUNCE   = 1U, /**< 按下消抖中             */
    KEY_STATE_HELD             = 2U, /**< 已确认按下，等待松开   */
    KEY_STATE_RELEASE_DEBOUNCE = 3U, /**< 松开消抖中             */
} Key_State_e;

/* ========================================================================== */
/*                              数据结构定义                                    */
/* ========================================================================== */

/**
 * @brief  单个按键状态机实例
 * @note   每个按键独立持有一个实例，调用前须将 state=0, cnt=0 清零初始化。
 */
typedef struct
{
    Key_State_e state; /**< 当前状态机状态 */
    uint8_t     cnt;   /**< 消抖计数器     */
} Key_t;

/* ========================================================================== */
/*                              函数声明                                        */
/* ========================================================================== */

/**
 * @brief  按键扫描状态机（Toggle 型）
 * @note   每次按下并完整释放后，将 *out_state 取反一次。
 *         须以固定周期调用（建议 1ms）。
 * @param  key        按键状态机实例指针
 * @param  key_level  当前按键 GPIO 电平，1=按下，0=松开
 * @param  out_state  输出状态指针，按键有效触发时取反
 * @retval 无
 */
void Key_Scan(Key_t *key, uint8_t key_level, uint8_t *out_state);

#endif /* KEY_SCAN_H */
